public class Q14 {
    public static void main(String[] args)
    {
        short shortValue = 124;

        // Convert short to byte
        byte byteValue = (byte) shortValue;

        // Convert short to short
        short shortValue2 = shortValue;

        // Convert short to int
        int intValue = shortValue;

        // Convert short to long
        long longValue = shortValue;

        // Convert short to float
        float floatValue = shortValue;

        // Convert short to double
        double doubleValue = shortValue;

        System.out.println("Short value: " + shortValue);
        System.out.println("Byte value: " + byteValue);
        System.out.println("Short value: " + shortValue2);
        System.out.println("Int value: " + intValue);
        System.out.println("Long value: " + longValue);
        System.out.println("Float value: " + floatValue);
        System.out.println("Double value: " + doubleValue);

    }
}
